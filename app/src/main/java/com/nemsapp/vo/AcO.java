package com.nemsapp.vo;

public class AcO {
    private int id = -1;
    private short unitNo;
    private short ptNo;
    private String sname;
    private String cname;
    private byte mask;
    private byte poinum;
    private float fi;
    private byte librank;
    private float fullV;
    private String lgName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public short getUnitNo() {
        return unitNo;
    }

    public void setUnitNo(short unitNo) {
        this.unitNo = unitNo;
    }

    public short getPtNo() {
        return ptNo;
    }

    public void setPtNo(short ptNo) {
        this.ptNo = ptNo;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public byte getMask() {
        return mask;
    }

    public void setMask(byte mark) {
        this.mask = mark;
    }

    public byte getPoinum() {
        return poinum;
    }

    public void setPoinum(byte poinum) {
        this.poinum = poinum;
    }

    public float getFi() {
        return fi;
    }

    public void setFi(float fi) {
        this.fi = fi;
    }

    public byte getLibrank() {
        return librank;
    }

    public void setLibrank(byte librank) {
        this.librank = librank;
    }

    public float getFullV() {
        return fullV;
    }

    public void setFullV(float fullV) {
        this.fullV = fullV;
    }

    public String getLgName() {
        return lgName;
    }

    public void setLgName(String lgName) {
        this.lgName = lgName;
    }

}
